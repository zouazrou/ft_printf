#include "ft_printf.h"

void	ft_str(char *s, size_t *len)
{
	size_t	i;

	if (!s)
	{
		write(1, "(null)", 6);
		(*len) += 6;
	}
	else
	{
		i = 0;
		while (s[i])
			ft_char(s[i++], len);
	}
}
